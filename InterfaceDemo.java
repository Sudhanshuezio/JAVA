interface X {
	int a = 50;
	void add();
	void sub();
}

interface Y {
	int b = 10;
	void mul();
	void div();
}

interface Z extends X,Y
{
}

class Calculator implements Z {
	public void add() {
		System.out.println(X.a + Y.b);
	}
	public void sub() {
		System.out.println(X.a - Y.b);
	}
	public void mul() {
		System.out.println(Z.a * Z.b);
	}
	public void div() {
		System.out.println(X.a / Y.b);
	}
}

public class InterfaceDemo {
	public static void main(String args[]) {
		X x1 = new Calculator();
		x1.add();
		x1.sub();
		System.out.println("------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");

		Y y1 = new Calculator();
		y1.mul();
		y1.div();
		System.out.println("------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
		
		Z z1 = new Calculator();
		z1.add();
		z1.sub();
		z1.mul();
		z1.div();
	}
}